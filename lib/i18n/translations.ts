// Pluralization for Arabic
export function pluralize(count: number, singular: string, plural: string, lang: 'ar' | 'en' = 'ar'): string {
  if (lang === 'ar') {
    // Arabic pluralization rules
    if (count === 0) return `لا يوجد ${singular}`;
    if (count === 1) return `${singular} واحد`;
    if (count === 2) return `${singular}ان`;
    if (count >= 3 && count <= 10) return `${count} ${plural}`;
    return `${count} ${singular}`;
  } else {
    return count === 1 ? `${count} ${singular}` : `${count} ${plural}`;
  }
}
